void	ft_putchar(char c);

void	rush(int	a, int	b)
{
	int	i;
	int	j;

	i = 1;
	while (i <= b)
	{
		j = 1;
		while (j <= a)
		{
			if ((i == 1 && j == 1) || (i == b && j == a && a != 1 && b != 1))
				ft_putchar('A');
			if ((i == b && j == 1 && b != 1) || (i == 1 && j == a && a != 1))
				ft_putchar('C');
			if (((j == 1 || j == a) && i != 1 && i != b)
				|| ((i == 1 || i == b) && j != a && j != 1))
				ft_putchar('B');
			else
				if (i != 1 && i != b && j != 1 && j != a)
					ft_putchar(' ');
			j++;
		}
		if (a > 0)
			ft_putchar('\n');
		i++;
	}
}
