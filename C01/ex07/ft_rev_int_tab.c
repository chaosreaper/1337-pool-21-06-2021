void	ft_rev_int_tab(int	*tab, int	size)
{
	int	rev;
	int	i;

	i = 0 ;
	while (i < size / 2)
	{
		rev = tab[i];
		tab[i] = tab[size - 1 - i];
		tab[size - 1 - i] = rev;
		i++;
	}
}
